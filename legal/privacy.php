 online browsing activity on other online services over time and we do
not permit third-party services to track your activity on our site
beyond our basic Google Analytics tracking, which you may opt out of <a href="https://tools.google.com/dlpage/gaoptout">here</a>.
 Because we do not share this kind of data with third party services or
permit this kind of third party data collection on Africa Trainovation Consulting for any of our
 users, and we do not track our users on third-party websites ourselves,
 we do not need to respond differently to an individual browser's Do Not
 Track setting.</p>

<p>If you are interested in turning on your browser’s privacy and Do Not Track settings, the <a href="http://donottrack.us/">Do Not Track</a> website has browser-specific instructions.</p>

<p>Please see our section on <a href="#how-we-communicate-with-you">email communication</a> to learn about our use of pixel tags in marketing emails.</p>

