<h3>
<a id="our-use-of-cookies-and-tracking" class="anchor" href="#our-use-of-cookies-and-tracking" aria-hidden="true"><span aria-hidden="true" class="octicon octicon-link"></span></a>Our use of cookies and tracking</h3>

<h4>
<a id="cookies" class="anchor" href="#cookies" aria-hidden="true"><span aria-hidden="true" class="octicon octicon-link"></span></a>Cookies</h4>

<p>Africa Trainovation Consulting uses cookies to make interactions with our service easy and
meaningful. We use cookies (and similar technologies, like HTML5
localStorage) to keep you logged in, remember your preferences, and
provide information for future development of Africa Trainovation Consulting.</p>

<p>A cookie is a small piece of text that our web server stores on your
computer or mobile device, which your browser sends to us when you
return to our site. Cookies do not necessarily identify you if you are
merely visiting Africa Trainovation Consulting; however, a cookie may store a unique identifier
for each logged in user. The cookies Africa Trainovation Consulting sets are essential for the
operation of the website, or are used for performance or functionality.
By using our website, you agree that we can place these types of cookies
 on your computer or device. If you disable your browser or device’s
ability to accept cookies, you will not be able to log in or use
Africa Trainovation Consulting’s services.</p>

<h4>
<a id="google-analytics" class="anchor" href="#google-analytics" aria-hidden="true"><span aria-hidden="true" class="octicon octicon-link"></span></a>Google Analytics</h4>

<p>We use Google Analytics as a third party tracking service, but we
don’t use it to track you individually or collect your User Personal
Information. We use Google Analytics to collect information about how
our website performs and how our users, in general, navigate through and
 use Africa Trainovation Consulting. This helps us evaluate our users' use of Africa Trainovation Consulting; compile
statistical reports on activity; and improve our content and website
performance.</p>

<p>Google Analytics gathers certain simple, non-personally identifying
information over time, such as your IP address, browser type, internet
service provider, referring and exit pages, time stamp, and similar data
 about your use of Africa Trainovation Consulting. We do not link this information to any of
your personal information such as your user name.</p>

<p>Africa Trainovation Consulting will not, nor will we allow any third party to, use the Google
 Analytics tool to track our users individually; collect any User
Personal Information other than IP address; or correlate your IP address
 with your identity. Google provides further information about its own
privacy practices and offers a <a href="https://tools.google.com/dlpage/gaoptout">browser add-on to opt out of Google Analytics tracking</a>.</p>

<p>Certain pages on our site may set other third party cookies. For
example, we may embed content, such as videos, from another site that
sets a cookie. While we try to minimize these third party cookies, we
can’t always control what cookies this third party content sets.</p>

