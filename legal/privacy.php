<h4>
<a id="dispute-resolution-process" class="anchor" href="#dispute-resolution-process" aria-hidden="true"><span aria-hidden="true" class="octicon octicon-link"></span></a>Dispute Resolution Process</h4>

<p>In the unlikely event that a dispute arises between you and Africa Trainovation Consulting
regarding our handling of your User Personal Information, we will do our
 best to resolve it. If we cannot, we have selected JAMS, an independent
 dispute resolution provider, to handle unresolved Privacy Shield
complaints. If we are unable to resolve your concerns after a good faith
 effort to address them, you may <a href="https://www.jamsadr.com/file-an-eu-us-privacy-shield-or-safe-harbor-claim">contact JAMS and submit a Privacy Shield claim</a>.
 JAMS is a US-based private alternate dispute resolution provider, and
we have contracted with JAMS to provide an independent recourse
mechanism for any of our users for privacy concerns <strong>at no cost to you.</strong>
 You do not need to appear in court; you may conduct this dispute
resolution process via telephone or video conference. If you are not
based in the EU or EEA, but you would still like to use the JAMS
arbitration process to resolve your dispute, please let us know and we
will provide access to you.</p>

