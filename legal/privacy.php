        <h2>Africa Trainovation Consulting Privacy Statement</h2>
          <p>Effective date: <strong>August 20, 2020</strong></p>

<p>Thanks for entrusting Africa Trainovation Consulting with your source code, your projects,
and your personal information. Holding onto your private information is a
 serious responsibility, and we want you to know how we're handling it.</p>

<h3>
<a id="the-short-version" class="anchor" href="#the-short-version" aria-hidden="true"><span aria-hidden="true" class="octicon octicon-link"></span></a>The short version</h3>

<p>We collect your information only with your consent; we only collect
the minimum amount of personal information that is necessary to fulfill
the purpose of your interaction with us; we don't sell it to third
parties; and we only use it as this Privacy Statement describes. If
you're visiting us from the EU: we comply with the <a href="https://www.privacyshield.gov/participant?id=a2zt000000001K2AAI">Privacy Shield framework</a>.</p>

<p>Of course, the short version doesn't tell you everything, so please read on for more details!</p>

<h3>
<a id="what-information-Africa-Trainovation-Consulting-collects-and-why" class="anchor" href="#what-information-Africa-Trainovation-Consulting-collects-and-why" aria-hidden="true"><span aria-hidden="true" class="octicon octicon-link"></span></a>What information Africa Trainovation Consulting collects and why</h3>

<h4>
<a id="information-from-website-browsers" class="anchor" href="#information-from-website-browsers" aria-hidden="true"><span aria-hidden="true" class="octicon octicon-link"></span></a>Information from website browsers</h4>

<p>If you're <strong>just browsing the website</strong>, we collect the
same basic information that most websites collect. We use common
internet technologies, such as cookies and web server logs. This is
stuff we collect from everybody, whether they have an account or not.</p>

<p>The information we collect about all visitors to our website includes
 the visitor’s browser type, language preference, referring site,
additional websites requested, and the date and time of each visitor
request. We also collect potentially personally-identifying information
like Internet Protocol (IP) addresses.</p>

<h5>
<a id="why-do-we-collect-this" class="anchor" href="#why-do-we-collect-this" aria-hidden="true"><span aria-hidden="true" class="octicon octicon-link"></span></a>Why do we collect this?</h5>

<p>We collect this information to better understand how our website
visitors use Africa Trainovation Consulting, and to monitor and protect the security of the
website.</p>

<h4>
<a id="information-from-users-with-accounts" class="anchor" href="#information-from-users-with-accounts" aria-hidden="true"><span aria-hidden="true" class="octicon octicon-link"></span></a>Information from users with accounts</h4>

<p>If you <strong>create an account</strong>, we require some basic
information at the time of account creation. You will create your own
user name and password, and we will ask you for a valid email account.
You also have the option to give us more information if you want to, and
 this may include "User Personal Information."</p>

<p>"User Personal Information" is any information about one of our users
 which could, alone or together with other information, personally
identify him or her. Information such as a user name and password, an
email address, a real name, and a photograph are examples of “User
Personal Information.”</p>

<p>User Personal Information does not include aggregated, non-personally
 identifying information. We may use aggregated, non-personally
identifying information to operate, improve, and optimize our website
and service.</p>

<h5>
<a id="why-do-we-collect-this-1" class="anchor" href="#why-do-we-collect-this-1" aria-hidden="true"><span aria-hidden="true" class="octicon octicon-link"></span></a>Why do we collect this?</h5>

<ul>
<li>We need your User Personal Information to create your account, and to provide the services you request.</li>
<li>We use your User Personal Information, specifically your user name, to identify you on Africa Trainovation Consulting.</li>
<li>We use it to fill out your profile and share that profile with other users if you ask us to.</li>
<li>We will use your email address to communicate with you, if you've said that's okay, <strong>and only for the reasons you’ve said that’s okay</strong>. Please see our section on <a href="#how-we-communicate-with-you">email communication</a> for more information.</li>
<li>We limit our use of your User Personal Information to the purposes
listed in this Privacy Statement. If we need to use your User Personal
Information for other purposes, we will ask your permission first. You
can always see what information we have, how we're using it, and what
permissions you have given us in your <a href="https://africatrainovationconsulting.com/settings/admin">user profile</a>.</li>
</ul>

<h3>
<a id="what-information-Africa-Trainovation-Consulting-does-not-collect" class="anchor" href="#what-information-Africa-Trainovation-Consulting-does-not-collect" aria-hidden="true"><span aria-hidden="true" class="octicon octicon-link"></span></a>What information Africa Trainovation Consulting does not collect</h3>

<p>We do not intentionally collect <strong>sensitive personal information</strong>,
 such as social security numbers, genetic data, health information, or
religious information. Although Africa Trainovation Consulting does not request or intentionally
 collect any sensitive personal information, we realize that you might
store this kind of information in your account, such as in a account.
 If you store any sensitive personal information on our servers, you are
 consenting to our storage of that information on our servers, which are
 in the United States.</p>

<p>We do not intentionally collect information that is <strong>stored in your repositories</strong>
 or other free-form content inputs. Information in your repositories
belongs to you, and you are responsible for it, as well as for making
sure that your content complies with our <a href="https://help.africatrainovationconsulting.com/articles/Africa-Trainovation-Consulting-terms-of-service/">Terms of Service</a>. Africa Trainovation Consulting employees <a href="https://help.africatrainovationconsulting.com/articles/Africa-Trainovation-Consulting-security/#employee-access">do not access private repositories unless required to</a> for security or maintenance, or for support reasons, with the consent of the account owner.</p>

<p>If your account is public, anyone (including us) may view its
contents. If you have included private or sensitive information in your
public account, such as email addresses, that information may be
indexed by search engines or used by third parties. In addition, while
we do not generally search for content in your repositories, we may scan
 our servers for certain tokens or security signatures.</p>

<p>If you're a <strong>child under the age of 13</strong>, you may not
have an account on Africa Trainovation Consulting. Africa Trainovation Consulting does not knowingly collect information
 from or direct any of our content specifically to children under 13. If
 we learn or have reason to suspect that you are a user who is under the
 age of 13, we will unfortunately have to close your account. We don't
want to discourage you from learning to code, but those are the rules.
Please see our <a href="https://help.africatrainovationconsulting.com/articles/Africa-Trainovation-Consulting-terms-of-service/">Terms of Service</a> for information about account termination.</p>

<h3>
<a id="how-we-share-the-information-we-collect" class="anchor" href="#how-we-share-the-information-we-collect" aria-hidden="true"><span aria-hidden="true" class="octicon octicon-link"></span></a>How we share the information we collect</h3>

<p>We <strong>do not</strong> share, sell, rent, or trade User Personal Information with third parties for their commercial purposes.</p>

<p>We do not disclose User Personal Information outside Africa Trainovation Consulting, except
in the situations listed in this section or in the section below on <a href="#how-we-respond-to-compelled-disclosure">Compelled Disclosure</a>.</p>

<p>We <strong>do</strong> share certain aggregated, non-personally
identifying information with others about how our users, collectively,
use Africa Trainovation Consulting, or how our users respond to our other offerings, such as our
 conferences or events. For example, we may <a href="https://africatrainovationconsulting.com/blog/1964-open-source-license-usage-on-Africa-Trainovation-Consulting-com">compile statistics on the usage of open source licenses across Africa Trainovation Consulting</a>. However, we do not sell this information to advertisers or marketers.</p>

<p>We do not host advertising on Africa Trainovation Consulting. We may occasionally embed
content from third party sites, such as YouTube, and that content may
include ads. While we try to minimize the amount of ads our embedded
content contains, we can't always control what third parties show.</p>

<p>We may share User Personal Information with your permission, so we can perform services you have requested.</p>

<p>We may share User Personal Information with a limited number of
third-party vendors who process it on our behalf to provide or improve
our service, and who have agreed to privacy restrictions similar to our
own Privacy Statement. Our vendors perform services such as payment
processing, customer support ticketing, network data transmission, and
other similar services. When we transfer your data to our vendors under <a href="https://help.africatrainovationconsulting.com/articles/Africa-Trainovation-Consulting-privacy-statement/#Africa Trainovation Consultings-global-privacy-practices">Privacy Shield</a>, we remain responsible for it.</p>

<p>We may share User Personal Information if we are involved in a
merger, sale, or acquisition. If any such change of ownership happens,
we will ensure that it is under terms that preserve the confidentiality
of User Personal Information, and we will notify you on our website or
by email before any transfer of your User Personal Information. The
organization receiving any User Personal Information will have to honor
any promises we have made in our Privacy Statement or in our Terms of
Service.</p>

